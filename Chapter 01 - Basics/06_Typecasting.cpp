#include <iostream>
using namespace std;

int main()
{
    int a = 45;
    cout<<"The value of a is "<<a<<endl;
    cout<<"The floating point value of a is "<<(float)a<<endl;
    float b = 45.56;
    cout<<"The value of b is "<<b<<endl;
    cout<<"The integral value of b is "<<(int)b;
    return 0;
}